const MainMint = ({ accounts, setAccounts }) => {
  const [mintAmount, setMintAmount] = useState(1);
  const isConnected = Boolean(accounts[0]);

  async function handleMint() {
    if (window.ethereum) {
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();
      const contract = new ethers.Contract(
        projectTreacherousAddress,
        abiarray,
        signer
      );
      try {
        const response = await contract.mint(BigNumber.from(mintAmount));
        console.log("response: ", response);
        setMinted(true);
      } catch (err) {
        console.log("error: ", err);
      }

      const supply = await contract.totalSupply();
      document.getElementById("supply").textContent = supply;
    }
  }

  const [minted, setMinted] = useState(false);

  const handleDecrement = () => {
    if (mintAmount <= 1) return;
    setMintAmount(mintAmount - 1);
  };

  const handleIncrement = () => {
    if (mintAmount <= 1) return;
    setMintAmount(mintAmount + 1);
  };

  return (
    <Flex justify="center" align="center" height="100vh" paddingBottom="150px">
      <Box width="520px">
        <div>
          <Text fontSize="48px" textShadow="0 5px #000000">
            Project Treacherous
          </Text>
          <Text
            fontSize="25px"
            letterSpacing="-5.5%"
            textShadow="0 2px 2px #000000"
          >
            //pt.1: the first 350 guardians of the horizon rise from the
            trenches, to defend their origin at all costs.
          </Text>
        </div>
        {isConnected ? (
          <div>
            <h1>
              <span id="supply"></span>/350
            </h1>
            <Flex align="center" justify="center">
              {!minted ? (
                <>
                  <Button
                    backgroundColor="#D6517D"
                    borderRadius="5px"
                    boxShadow="0px 2px 2px 1px #0F0F0F"
                    color="white"
                    cursor="pointer"
                    fontFamily="inherit"
                    padding="15px"
                    marginTop="10px"
                    onClick={handleDecrement}
                  >
                    -
                  </Button>
                  <Input
                    readOnly
                    fontFamily="inherit"
                    width="100px"
                    height="40px"
                    textAlign="center"
                    paddingLeft="19px"
                    marginTop="10px"
                    type="number"
                    value={mintAmount}
                  />
                  <Button
                    backgroundColor="#D6517D"
                    borderRadius="5px"
                    boxShadow="0px 2px 2px 1px #0F0F0F"
                    color="white"
                    cursor="pointer"
                    fontFamily="inherit"
                    padding="15px"
                    marginTop="10px"
                    onClick={handleIncrement}
                  >
                    +
                  </Button>
                </>
              ) : (
                <p>Congratulations on minting!</p>
              )}
            </Flex>
            <Button
              backgroundColor="#D6517D"
              borderRadius="5px"
              boxShadow="0px 2px 2px 1px #0F0F0F"
              color="white"
              cursor="pointer"
              fontFamily="inherit"
              padding="15px"
              marginTop="10px"
              onClick={handleMint}
            >
              Mint Now
            </Button>
          </div>
        ) : (
          <p>You must be connected to Mint.</p>
        )}
      </Box>
    </Flex>
  );
};

export default MainMint;
